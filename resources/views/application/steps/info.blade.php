    <h5 class="text-primary">1. Submitting my application ?</h5>
    <p>
      Submit your application as soon as you can to ensure there are no delays.
      The closing date for applications is 31st March 2020; applications will not be accepted after this date.
      If you have failed your MPharm/OSPAP and will be re-sitting please note that your application should be submitted
      by
      the deadline (31st March 2020).
    </p>

    <h5 class="text-primary">2. How can I pay ?</h5>
    <p>
      Payment can be made by debit card.
    </p>

    <h5 class="text-primary">3. When can I start pre-registration training ?</h5>
    <p>
      For the 2020-21 intake, pre-registration training may only start between 15 July 2020* and 14 September 2020.
      *If you start training before this date it will not count towards your 52 week total.
    </p>

    <h5 class="text-primary">4. Birth certificate ?</h5>
    <p>
      The Pharmaceutical Society NI requires a certified photocopy of your original birth certificate to be uploaded
      during your online application.
    </p>

    <h5 class="text-primary">5. I have not yet passed my MPharm/OSPAP ?</h5>
    <p>
      You can apply to start training before you know if you have passed your MPharm/OSPAP.
      You cannot start training until you have passed your MPharm/OSPAP and the Pharmaceutical Society NI has been
      notified.
    </p>

    <h5 class="text-primary">6. I have failed my MPharm/OSPAP and will be re-sitting ?</h5>
    <p>
      You cannot start training until you have passed your MPharm/OSPAP, therefore you will have to delay your start
      date.
      If you have failed you should contact your employer and make them aware and contact us to arrange an alternative
      start date within the designated period.
    </p>

