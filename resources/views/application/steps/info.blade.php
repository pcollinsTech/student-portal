    <h5 class="text-primary">9. What do I do if I have been assigned two tutors?</h5>
    <p>
      Co-tutoring is permitted as long as, between the two tutors, the total hours worked meets the full-time
      requirement
      (30 hours over a minimum of four days). Both tutors must complete the ‘Tutor Details’ section and Learning
      Contract,
      they will share the responsibility and must co-sign the final declaration. A co-tutoring form must be completed
      and
      submitted with the application. The co-tutoring form can be accessed via link below:
